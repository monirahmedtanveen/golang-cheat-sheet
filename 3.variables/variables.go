package main

import "fmt"

func main() {
  var a = "Initialize"
  fmt.Println("a=", a)

  var b, c int = 1, 2
  fmt.Println("b=", b, ", c=", c)

  var d = 20.56
  fmt.Println("d=", d)

  var e = true
  fmt.Println("e=", e)

  var f int
  fmt.Println("f=", f)

  var g string = "Hello Golang!!"
  fmt.Println("g=", g)

  h := "apple mac pro!!"
  fmt.Println("h=", h)
}
